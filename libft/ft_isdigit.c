#include "libft.h"

int	ft_isdigit(char *c)
{
	int	i;

	i = 0;
	while (c[i])
	{
		if (c[i] >= 48 && c[i] <= 57)
		{
			return (1);
		}
		i++;
	}
	return (0);
}
